#include "car_motion.h"

//-------------------------------------------------------------------------------------------------------------------
// @brief        小车前进
// @param        void
// @return       void
// Sample usage:        car_forward();
                        //小车前进
//-----------------------------------------------------------------------------------------------------------------
void car_forward(void)
{
    motor_forward(left,5000);
    motor_forward(right,5000);
}

//-------------------------------------------------------------------------------------------------------------------
// @brief        小车后退
// @param        void
// @return       void
// Sample usage:        car_back();
                        //小车后退
//-----------------------------------------------------------------------------------------------------------------
void car_back(void)
{
    motor_back(left,5000);
    motor_back(right,5000);
}

//-------------------------------------------------------------------------------------------------------------------
// @brief        小车停止
// @param        void
// @return       void
// Sample usage:        car_stop();
                        //小车停止
//-----------------------------------------------------------------------------------------------------------------
void car_stop(void)
{
    motor_forward(left,0);
    motor_forward(right,0);
}

//-------------------------------------------------------------------------------------------------------------------
// @brief        小车左转
// @param        void
// @return       void
// Sample usage:        car_turnleft();
                        //小车左转
//-----------------------------------------------------------------------------------------------------------------
void car_turnleft(void)
{
    motor_forward(left,2500);
    motor_forward(right,7000);
}

//-------------------------------------------------------------------------------------------------------------------
// @brief        小车右转
// @param        void
// @return       void
// Sample usage:        car_turnright();
                        //小车右转
//-----------------------------------------------------------------------------------------------------------------
void car_turnright(void)
{
    motor_forward(left,7000);
    motor_forward(right,2000);
}
/*用作圆环*/
void circle_forward(void)
{
    motor_forward(left,7000);
    motor_forward(right,7000);
}
